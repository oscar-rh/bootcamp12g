function suma()
{

    let spanNumero = document.querySelector('#number')
    let numero = Number(spanNumero.innerHTML)
    numero = numero + 1
    spanNumero.textContent = numero    
}


function resta()
{
    let spanNumero = document.querySelector('#number')
    let numero = Number(spanNumero.innerHTML)
    numero = numero - 1
    spanNumero.textContent = numero    
}
 




